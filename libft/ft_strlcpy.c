#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	ret;

	ret = ft_strlen(src);
	if (size == 0)
		return (ret);
	while (--size && *src != '\0')
		*dest++ = *src++;
	*dest = '\0';
	return (ret);
}
